numOfBottles = 9

while numOfBottles >= 3
  puts "#{numOfBottles} bottles of beer on the wall, #{numOfBottles} bottles of beer"
  puts "Take one down and pass it around, there'll be #{numOfBottles - 1} bottles of beer on the wall"
  numOfBottles -= 1
end
puts "2 bottles of beer on the wall, 2 bottles of beer"
puts "Take one down and pass it around, there'll be 1 bottle of beer on the wall"
puts "1 bottle of beer on the wall, 1 bottle of beer"
puts "Take one down and pass it around, there'll be no bottles of beer on the wall"
